# Build the Dash app
# ---------------------------------------------------------------------------

def build_app(all_samples: list[dict], run_tag: str) -> Dash:
    # Group by dataset, filter to binary with_hds datasets for the main scatter
    datasets: dict[str, list[dict]] = {}
    for s in all_samples:
        datasets.setdefault(s["dataset"], []).append(s)

    # Filter to binary datasets with enough samples
    binary_datasets = {
        k: v for k, v in sorted(datasets.items())
        if len(set(s["label"] for s in v)) == 2 and len(v) >= 20
    }

    dataset_names = list(binary_datasets.keys())

    app = Dash(__name__)

    app.layout = html.Div(
        style={"fontFamily": "monospace", "maxWidth": "1400px", "margin": "0 auto",
               "padding": "20px"},
        children=[
            html.H2(f"ICL Diversity Audit — {run_tag}"),
            html.P("Click a point to inspect. Hover for summary."),

            # Dataset selector
            html.Label("Dataset:"),
            dcc.Dropdown(
                id="dataset-dropdown",
                options=[{"label": n, "value": n} for n in dataset_names],
                value=dataset_names[0] if dataset_names else None,
                style={"width": "500px", "marginBottom": "10px"},
            ),

            # Metric selector for Y axis
            html.Label("Y axis:"),
            dcc.Dropdown(
                id="metric-dropdown",
                options=[
                    {"label": "C × a_n (pb)", "value": "C_a_n_pb"},
                    {"label": "a_n (pb)", "value": "a_n_pb"},
                    {"label": "C (coherence)", "value": "C"},
                    {"label": "E (excess entropy)", "value": "E"},
                    {"label": "σ_ℓ (coherence spread)", "value": "sigma"},
                ],
                value="C_a_n_pb",
                style={"width": "300px", "marginBottom": "20px"},
            ),

            # Main scatter plot
            dcc.Graph(id="scatter-plot", style={"height": "500px"}),

            # Detail panel (updates on click)
            html.Hr(),
            html.H3(id="detail-title", children="Click a point to inspect"),
            html.Div(id="detail-panel", style={"display": "flex", "gap": "30px"}, children=[
                # Left: a_k curve
                html.Div(style={"flex": "1"}, children=[
                    dcc.Graph(id="ak-curve-plot", style={"height": "350px"}),
                ]),
                # Right: metrics + text
                html.Div(style={"flex": "1.5", "overflowY": "auto", "maxHeight": "600px"}, children=[
                    html.Pre(id="metrics-text",
                             style={"fontSize": "13px", "whiteSpace": "pre-wrap"}),
                    html.Hr(),
                    html.H4("Responses"),
                    html.Pre(id="responses-text",
                             style={"fontSize": "12px", "whiteSpace": "pre-wrap",
                                    "maxHeight": "400px", "overflowY": "auto",
                                    "backgroundColor": "#f5f5f5", "padding": "10px"}),
                ]),
            ]),

            # Per-token logprobs section (only if model loaded)
            html.Hr(),
            html.H3("Per-Token Logprobs"),
            html.Div(
                children=[
                    html.Label("Response position k:"),
                    dcc.Dropdown(
                        id="k-position-dropdown",
                        options=[{"label": f"k={i+1}", "value": i} for i in range(10)],
                        value=0,
                        style={"width": "120px", "display": "inline-block", "marginRight": "10px"},
                    ),
                    html.Button(
                        "Compute per-token logprobs",
                        id="compute-logprobs-btn",
                        disabled=_MODEL is None,
                        style={"marginBottom": "10px"},
                    ),
                    html.Span(
                        " (no model loaded — use --device cuda:0)" if _MODEL is None else "",
                        style={"color": "gray", "fontSize": "12px"},
                    ),
                ],
                style={"marginBottom": "10px"},
            ),
            dcc.Loading(
                children=[dcc.Graph(id="logprobs-plot", style={"height": "400px"})],
                type="circle",
            ),

            # Hidden store for full sample data (JSON)
            dcc.Store(id="samples-store"),
        ],
    )

    # Store sample data as JSON for client-side access
    @app.callback(
        Output("samples-store", "data"),
        Input("dataset-dropdown", "value"),
    )
    def update_store(dataset_name: str | None) -> list[dict] | None:
        if dataset_name is None:
            return None
        samples = binary_datasets.get(dataset_name, [])
        # Serialize — include everything needed for detail panel
        return [
            {
                "idx": i,
                "sidecar_key": s["sidecar_key"],
                "label": s["label"],
                "context": s["context"],
                "responses": s["responses"],
                "C": s["C"],
                "a_n_pb": s["a_n_pb"],
                "C_a_n_pb": s["C_a_n_pb"],
                "E": s["E"],
                "E_rate": s["E_rate"],
                "D": s["D"],
                "sigma": s["sigma"],
                "mean_byte_length": s["mean_byte_length"],
                "a_k_pb": s["a_k_pb"],
                "a_k_bits": s["a_k_bits"],
                "uncond_pb": s["uncond_pb"],
                "uncond_bits": s["uncond_bits"],
            }
            for i, s in enumerate(samples)
        ]

    @app.callback(
        Output("scatter-plot", "figure"),
        Input("samples-store", "data"),
        Input("metric-dropdown", "value"),
    )
    def update_scatter(
        samples_data: list[dict] | None, metric_key: str,
    ) -> go.Figure:
        if not samples_data:
            return go.Figure()

        fig = go.Figure()
        for label_val, name, color in [
            (0.0, "Low diversity", "rgba(31,119,180,0.5)"),
            (1.0, "High diversity", "rgba(214,39,40,0.5)"),
        ]:
            group = [s for s in samples_data if s["label"] == label_val]
            if not group:
                continue

            rng = np.random.default_rng(42)
            jitter = rng.uniform(-0.15, 0.15, len(group))
            x = [label_val + j for j in jitter]
            y = [s[metric_key] for s in group]
            custom = [s["idx"] for s in group]

            hover_texts = []
            for s in group:
                resp_preview = " | ".join(
                    r[:40] + "..." if len(r) > 40 else r for r in s["responses"]
                )
                hover_texts.append(
                    f"<b>{s['sidecar_key']}</b><br>"
                    f"C={s['C']:.4f}, a_n={s['a_n_pb']:.3f}, "
                    f"C×a_n={s['C_a_n_pb']:.3f}<br>"
                    f"E={s['E']:.1f}, σ={s['sigma']:.3f}<br>"
                    f"{resp_preview[:200]}"
                )

            fig.add_trace(go.Scatter(
                x=x, y=y,
                mode="markers",
                marker=dict(size=8, color=color, line=dict(width=0.5, color="white")),
                name=name,
                customdata=custom,
                hovertext=hover_texts,
                hoverinfo="text",
            ))

        fig.update_layout(
            xaxis=dict(
                tickvals=[0, 1],
                ticktext=["Low div", "High div"],
                range=[-0.4, 1.4],
            ),
            yaxis_title=metric_key,
            hovermode="closest",
            margin=dict(l=60, r=20, t=40, b=40),
        )
        return fig

    @app.callback(
        Output("detail-title", "children"),
        Output("metrics-text", "children"),
        Output("responses-text", "children"),
        Output("ak-curve-plot", "figure"),
        Input("scatter-plot", "clickData"),
        Input("samples-store", "data"),
    )
    def update_detail(
        click_data: dict | None,
        samples_data: list[dict] | None,
    ) -> tuple[str, str, str, go.Figure]:
        if click_data is None or not samples_data:
            return (
                "Click a point to inspect",
                "",
                "",
                go.Figure(),
            )

        pt = click_data["points"][0]
        idx = pt.get("customdata")
        if idx is None:
            return ("No data for this point", "", "", go.Figure())

        s = samples_data[idx]

        # Title
        label_name = "HIGH diversity" if s["label"] == 1.0 else "LOW diversity"
        title = f"{s['sidecar_key']} — {label_name}"

        # Metrics text
        metrics_lines = [
            f"C (coherence)      = {s['C']:.4f}",
            f"a_n (pb)           = {s['a_n_pb']:.4f}",
            f"C × a_n (pb)       = {s['C_a_n_pb']:.4f}",
            f"E (excess entropy) = {s['E']:.2f} bits",
            f"E_rate             = {s['E_rate']:.4f} bits/byte",
            f"D (C × E)          = {s['D']:.2f} bits",
            f"σ_ℓ (coh. spread)  = {s['sigma']:.4f}",
            f"mean byte length   = {s['mean_byte_length']:.1f}",
            "",
            f"Context: \"{s['context']}\"",
        ]

        # Unconditional surprises
        if s.get("uncond_pb"):
            metrics_lines.append("")
            metrics_lines.append("Unconditional surprise (pb) per response:")
            for i, u in enumerate(s["uncond_pb"]):
                metrics_lines.append(f"  resp_{i}: {u:.4f} bits/byte")

        metrics_text = "\n".join(metrics_lines)

        # Response text
        resp_lines = []
        for i, r in enumerate(s["responses"]):
            resp_lines.append(f"--- Response {i} ---")
            resp_lines.append(r)
            resp_lines.append("")
        responses_text = "\n".join(resp_lines)

        # a_k curve plot
        a_k_pb = s["a_k_pb"]
        a_k_bits = s["a_k_bits"]
        k = list(range(1, len(a_k_pb) + 1))

        fig = make_subplots(rows=1, cols=2, subplot_titles=["a_k (bits/byte)", "a_k (total bits)"])

        fig.add_trace(
            go.Scatter(x=k, y=a_k_pb, mode="lines+markers", name="mean (pb)",
                       marker=dict(size=8, color="red"), line=dict(width=2)),
            row=1, col=1,
        )
        fig.add_trace(
            go.Scatter(x=k, y=a_k_bits, mode="lines+markers", name="mean (bits)",
                       marker=dict(size=8, color="blue"), line=dict(width=2)),
            row=1, col=2,
        )

        fig.update_xaxes(title_text="k", dtick=1, row=1, col=1)
        fig.update_xaxes(title_text="k", dtick=1, row=1, col=2)
        fig.update_yaxes(title_text="bits/byte", row=1, col=1)
        fig.update_yaxes(title_text="bits", row=1, col=2)
        fig.update_layout(
            margin=dict(l=50, r=20, t=40, b=40),
            showlegend=False,
        )

        return title, metrics_text, responses_text, fig

    @app.callback(
        Output("logprobs-plot", "figure"),
        Input("compute-logprobs-btn", "n_clicks"),
        State("scatter-plot", "clickData"),
        State("samples-store", "data"),
        State("k-position-dropdown", "value"),
        prevent_initial_call=True,
    )
    def compute_logprobs(
        n_clicks: int | None,
        click_data: dict | None,
        samples_data: list[dict] | None,
        k_pos: int,
    ) -> go.Figure:
        if not n_clicks or click_data is None or not samples_data:
            return no_update

        pt = click_data["points"][0]
        idx = pt.get("customdata")
        if idx is None:
            return no_update

        s = samples_data[idx]
        n_responses = len(s["responses"])
        if k_pos >= n_responses:
            k_pos = n_responses - 1

        fig = compute_per_token_logprobs(s["context"], s["responses"], k_pos)
        if fig is None:
            return go.Figure().add_annotation(
                text="No model loaded. Restart with --device cuda:0",
                xref="paper", yref="paper", x=0.5, y=0.5,
                showarrow=False, font=dict(size=16),
            )
        return fig

    return app


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> None:
    global _MODEL, _TOKENIZER, _DEVICE

    parser = argparse.ArgumentParser(description="Interactive scatter for auditing diversity metrics")
    parser.add_argument("--run-tag", type=str, default="qwen25_completion_v3")
    parser.add_argument("--port", type=int, default=8050)
    parser.add_argument("--device", type=str, default=None,
                        help="Device for per-token logprobs (e.g., cuda:0). "
                             "Omit for no model loading (faster startup).")
    parser.add_argument("--base-model", type=str, default="Qwen/Qwen2.5-3B",
                        help="Model for per-token logprobs (default: Qwen/Qwen2.5-3B)")
    args = parser.parse_args()

    data_dir = RESULTS_BASE / args.run_tag
    if not data_dir.exists():
        print(f"No data at {data_dir}")
        sys.exit(1)

    # Load model if --device is specified
    if args.device is not None:
        import torch
        from transformers import AutoModelForCausalLM, AutoTokenizer

        print(f"Loading model {args.base_model} on {args.device}...")
        _TOKENIZER = AutoTokenizer.from_pretrained(args.base_model)
        _MODEL = AutoModelForCausalLM.from_pretrained(
            args.base_model,
            torch_dtype=torch.float16,
            device_map=args.device,
        )
        _MODEL.eval()
        _DEVICE = args.device
        torch.set_grad_enabled(False)
        print("Model loaded.")

    print(f"Loading data from {data_dir}...")
    all_samples = load_all_samples(data_dir, args.run_tag)
    print(f"Loaded {len(all_samples)} samples")

    app = build_app(all_samples, args.run_tag)

    print(f"\nStarting Dash app on http://0.0.0.0:{args.port}")
    print(f"Open http://localhost:{args.port} in your laptop browser")
    print("(requires SSH port forwarding: ssh -L {0}:localhost:{0} ...)".format(args.port))
    if _MODEL is not None:
        print("Per-token logprobs: ENABLED (click a point, then click 'Compute')")
    else:
        print("Per-token logprobs: DISABLED (use --device cuda:0 to enable)")
    app.run(host="0.0.0.0", port=args.port, debug=False)


if __name__ == "__main__":
    main()
