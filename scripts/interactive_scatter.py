# ---------------------------------------------------------------------------
# Data loading (same patterns as analyze_c_ainf.py)
# ---------------------------------------------------------------------------

def load_sidecar(sidecar_path: Path) -> dict[str, dict]:
    with open(sidecar_path) as f:
        data = json.load(f)
    return {k: v for k, v in data.items() if not k.startswith("__")}


def load_csv_rows(csv_path: Path) -> dict[str, dict]:
    with open(csv_path, newline="", encoding="utf-8") as f:
        rows = list(csv.DictReader(f))
    key_col = "sidecar_key" if rows and "sidecar_key" in rows[0] else "sample_id"
    return {row[key_col]: row for row in rows}


def compute_per_byte_curve(entry: dict) -> list[float] | None:
    perm_curves = entry.get("per_permutation_a_k_curves")
    perm_bytes = entry.get("per_permutation_byte_counts")
    if perm_curves is None or perm_bytes is None:
        return entry.get("a_k_curve_per_byte")
    per_byte_curve: list[float] = []
    for k_idx in range(len(perm_curves[0])):
        ratios = []
        for p in range(len(perm_curves)):
            bits = perm_curves[p][k_idx]
            bc = perm_bytes[p][k_idx]
            if bc > 0:
                ratios.append(bits / bc)
        per_byte_curve.append(sum(ratios) / len(ratios) if ratios else 0.0)
    return per_byte_curve


# ---------------------------------------------------------------------------
# Per-token logprob computation (requires model)
# ---------------------------------------------------------------------------

def _format_token_label(tok: str) -> str:
    """Format token for display. ▁ prefix for leading space, ↵ for newlines."""
    if tok.startswith(" "):
        return "▁" + tok[1:].replace("\n", "↵").replace("\t", "→")
    t = tok.replace("\n", "↵").replace("\t", "→")
    if not t.strip():
        return repr(tok).strip("'").replace("\\n", "↵").replace("\\t", "→")
    return t


def compute_per_token_logprobs(
    context: str, responses: list[str], k_pos: int,
) -> go.Figure | None:
    """Compute per-token logprob bar chart for position k_pos.

    Returns a Plotly figure, or None if no model is loaded.
    Uses the global _MODEL, _TOKENIZER loaded at startup.
    """
    if _MODEL is None or _TOKENIZER is None:
        return None

    import torch
    from icl_diversity.core import (
        FormatMode,
        _ensure_models,
        _find_response_boundaries,
        _forward_log_probs,
        format_conditioning_context,
    )

    models = _ensure_models(_MODEL)
    format_mode: FormatMode = "completion"

    previous = responses[:k_pos]
    current = responses[k_pos]
    prefix, target = format_conditioning_context(
        context, previous, current, format_mode=format_mode,
    )

    full_ids, boundaries = _find_response_boundaries(
        _TOKENIZER, context, responses[:k_pos + 1], format_mode=format_mode,
    )

    input_ids = torch.tensor([full_ids], device=_DEVICE)
    with torch.no_grad():
        log_probs = _forward_log_probs(models, input_ids)[0]  # (seq_len,) in bits

    n_tokens = len(full_ids)
    surprises = np.zeros(n_tokens)
    for t in range(1, n_tokens):
        surprises[t] = -log_probs[t - 1].item()

    # Build mask
    is_unmasked = np.zeros(n_tokens, dtype=bool)
    for start, end in boundaries:
        for t in range(start, end):
            is_unmasked[t] = True

    # Decode tokens
    token_strings = [_TOKENIZER.decode([tid]) for tid in full_ids]
    display_tokens = [_format_token_label(t) for t in token_strings]
    colors = ["#d32f2f" if u else "#9e9e9e" for u in is_unmasked]

    # Current response total bits
    cur_bits = 0.0
    if k_pos < len(boundaries):
        cur_start, cur_end = boundaries[k_pos]
        cur_bits = sum(surprises[t] for t in range(cur_start, cur_end))

    fig = go.Figure()
    fig.add_trace(go.Bar(
        x=list(range(n_tokens)),
        y=surprises.tolist(),
        marker_color=colors,
        hovertext=[f"{dt}: {s:.2f} bits" for dt, s in zip(display_tokens, surprises)],
        hoverinfo="text",
    ))

    # Response boundary lines
    for bk, (start, end) in enumerate(boundaries):
        if start > 0:
            fig.add_vline(x=start - 0.5, line_dash="dash", line_color="blue",
                          line_width=1, opacity=0.5)
        mid = (start + end) / 2
        fig.add_annotation(
            x=mid, y=max(surprises) * 0.95 if max(surprises) > 0 else 5,
            text=f"r_{bk+1}", showarrow=False, font=dict(size=10, color="blue"),
        )

    fig.update_layout(
        xaxis=dict(
            tickvals=list(range(n_tokens)),
            ticktext=display_tokens,
            tickangle=90,
            tickfont=dict(size=9, family="monospace"),
        ),
        yaxis_title="Surprise (bits)",
        title=f"k={k_pos+1}: conditioning on {k_pos} responses — "
              f"current response total: {cur_bits:.1f} bits",
        margin=dict(l=50, r=20, t=40, b=120),
        height=350,
        showlegend=False,
    )

    return fig


# ---------------------------------------------------------------------------
# Load all data into a flat list of sample dicts
# ---------------------------------------------------------------------------

def load_all_samples(
    data_dir: Path, tag: str,
) -> list[dict]:
    """Load all samples with metrics, curves, and response text."""
    all_samples: list[dict] = []

    for sidecar_path in sorted(data_dir.rglob(f"*.icl_curves.{tag}.json")):
        csv_stem = sidecar_path.name.replace(f".icl_curves.{tag}.json", "")
        csv_path = sidecar_path.with_name(csv_stem + ".csv")
        if not csv_path.exists():
            continue

        dataset_key = f"{sidecar_path.parent.name}/{csv_stem}"
        sidecar = load_sidecar(sidecar_path)
        csv_rows = load_csv_rows(csv_path)

        resp_cols = sorted(
            [c for c in next(iter(csv_rows.values())) if c.startswith("resp_")]
        )

        for sid, entry in sidecar.items():
            row = csv_rows.get(sid)
            if row is None:
                continue

            label = float(row.get("label_value", "nan"))
            if np.isnan(label):
                continue

            metrics = entry.get("metrics", {})
            C = metrics.get("coherence_C", 0.0)
            a_k_pb = compute_per_byte_curve(entry)
            a_k_bits = entry.get("a_k_curve", [])

            if not a_k_pb or len(a_k_pb) < 2:
                continue

            a_n_pb = a_k_pb[-1]
            C_a_n_pb = C * a_n_pb

            responses = [row.get(c, "") for c in resp_cols]
            context = row.get("context", "")

            all_samples.append({
                "sidecar_key": sid,
                "dataset": dataset_key,
                "label": label,
                "context": context,
                "responses": responses,
                # Metrics
                "C": C,
                "a_n_pb": a_n_pb,
                "C_a_n_pb": C_a_n_pb,
                "E": metrics.get("excess_entropy_E", 0.0),
                "E_rate": metrics.get("excess_entropy_E_rate", 0.0),
                "D": metrics.get("diversity_score_D", 0.0),
                "sigma": metrics.get("coherence_spread_sigma", 0.0),
                "mean_byte_length": metrics.get("mean_byte_length", 0.0),
                # Curves
                "a_k_pb": a_k_pb,
                "a_k_bits": a_k_bits,
                "per_perm_curves": entry.get("per_permutation_a_k_curves"),
                "per_perm_bytes": entry.get("per_permutation_byte_counts"),
                "uncond_pb": entry.get("unconditional_surprises", []),
                "uncond_bits": entry.get("unconditional_total_bits", []),
            })

    return all_samples


