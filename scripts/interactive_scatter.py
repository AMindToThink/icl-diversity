"""Interactive scatter plot for auditing C×a_n diversity metric results.

Dash app that lets you click on individual data points to inspect:
- The sample's a_k curve (per-byte, with per-permutation curves)
- Summary statistics (E, C, σ_ℓ, a_n, etc.)
- Full response text
- Per-token logprob bar charts (requires --device for GPU model loading)

Requires SSH port forwarding to view on a remote machine:
    ssh -L 8050:localhost:8050 user@server

Data provenance:
- Sidecar JSONs: results/tevet/{run_tag}/*/*.icl_curves.{run_tag}.json
- CSVs: results/tevet/{run_tag}/*/*.csv
- Generated by: scripts/compute_icl_metrics_for_tevet.py

Usage:
    # Without per-token logprobs (fast startup, no GPU needed):
    uv run python scripts/interactive_scatter.py --run-tag qwen25_completion_v3

    # With per-token logprobs (loads model on startup, ~30s):
    uv run python scripts/interactive_scatter.py --run-tag qwen25_completion_v3 --device cuda:0

    # Then open http://localhost:8050 in your laptop browser
"""

